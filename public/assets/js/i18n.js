(function () {
  function getLang() {
    return location.pathname.includes("/en/") ? "en" : "ne";
  }
  function setLang(lang) {
    // Kept only so the root redirect page can remember the visitor's last
    // choice for next time. It no longer switches the current page: with
    // separate /ne/ and /en/ page sets, language is determined by which
    // page you're on, and switching means navigating to the sibling page.
    localStorage.setItem("npd-lang", lang);
    document.documentElement.setAttribute("lang", lang === "en" ? "en" : "ne");
  }
  function t(key) {
    const lang = getLang();
    return (I18N[lang] && I18N[lang][key]) || I18N.ne[key] || key;
  }
  // Apply data-i18n="key" text content + data-i18n-placeholder="key" on load.
  function applyStatic(root = document) {
    root.querySelectorAll("[data-i18n]").forEach(el => {
      el.textContent = t(el.getAttribute("data-i18n"));
    });
    // For the handful of strings that need inline markup (e.g. <strong>,
    // <em>) rather than plain text. Values are static, author-controlled
    // dictionary entries, never user input, so this is not an XSS risk.
    root.querySelectorAll("[data-i18n-html]").forEach(el => {
      el.innerHTML = t(el.getAttribute("data-i18n-html"));
    });
    root.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
      el.setAttribute("placeholder", t(el.getAttribute("data-i18n-placeholder")));
    });
    root.querySelectorAll("[data-i18n-aria-label]").forEach(el => {
      el.setAttribute("aria-label", t(el.getAttribute("data-i18n-aria-label")));
    });
  }
  window.I18N_ENGINE = { getLang, setLang, t, applyStatic };
})();
